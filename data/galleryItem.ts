export const getGalleryItemsByMonth = (year: number, month: number): GalleryItem[] => {
    return galleryItems.filter((item) => {
        const itemDate = new Date(item.date)
        return itemDate.getFullYear() === year && itemDate.getMonth() + 1 === month
    })
}

export const getAvailableMonths = (): { year: number; month: number;}[] => {
    const monthMap = new Map<string, number>()
    
    galleryItems.forEach((item) => {
        const date = new Date(item.date)
        const key = `${date.getFullYear()}-${date.getMonth() + 1}`
        monthMap.set(key, (monthMap.get(key) || 0) + 1)
    })
    
    return Array.from(monthMap.entries())
        .map(([key]) => {
        const [year, month] = key.split("-").map(Number)
        return { year, month}
        })
        .sort((a, b) => {
        // 최신순 정렬
        if (a.year !== b.year) return b.year - a.year
        return b.month - a.month
    })
}

export const groupGalleryItemsByMonth = (items: GalleryItem[]): Record<string, GalleryItem[]> => {
    const groups: Record<string, GalleryItem[]> = {}

    items.forEach((item) => {
        const date = new Date(item.date)
        const key = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, "0")}`

        if (!groups[key]) {
        groups[key] = []
    }

    groups[key].push(item)
    })

// 각 그룹 내에서도 최신순 정렬
    Object.keys(groups).forEach((key) => {
        groups[key].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    })

    return groups
}

// 갤러리 아이템 관련 유틸리티 함수들
export const getGalleryItemById = (id: number): GalleryItem | undefined => {
    return galleryItems.find((item) => item.id === id)
}

export const getGalleryItemsByCategory = (mainCategory: string, subCategory?: string): GalleryItem[] => {
    return galleryItems.filter((item) => {
        if (item.mainCategory !== mainCategory) return false
        if (subCategory && !item.subCategories.includes(subCategory)) return false
        return true
    })
}

export const getGalleryItemsByDateRange = (startDate: string, endDate: string): GalleryItem[] => {
    const start = new Date(startDate)
    const end = new Date(endDate)

    return galleryItems.filter((item) => {
        const itemDate = new Date(item.date)
        return itemDate >= start && itemDate <= end
    })
}

export const searchGalleryItems = (query: string): GalleryItem[] => {
    const lowercaseQuery = query.toLowerCase()

    return galleryItems.filter((item) =>
        item.title.toLowerCase().includes(lowercaseQuery),
    )
}

// 항상 최신순으로 정렬 (날짜 기준)
export const sortGalleryItems = (items: GalleryItem[]): GalleryItem[] => {
    return [...items].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
}
  
